package com.class2;

public class StringConcatenation {

	public static void main(String[] args) {

		String name = "John";

		// My name is John;

		System.out.println("My name is " + name);

		String day = "Sunday";
		// Sunday is a coding day

		System.out.println(day + " is a coding day");

		String season = "Winter";
		// Winter is very cold

		System.out.println(season + " is very cold");

		String key = "practice";
		// The key for success is practice
		System.out.println("The key for success is " + key);

		int num1 = 12;
		int num2 = 13;
		// The value of num1 is_ and the value of num2 is_

		System.out.println("The value of num1 is " + num1 + " and the value of num2 is " + num2);

		String lastName = "Smith";
		char grade='A';
		String city="Reston";
		String state="VA";
		String phoneNumber="1234567890";
		
		
		
		/* 
		 * print 
		 * My name is _ and my last name is _ 
		 * I am _ student 
		 * I live in city of _ in a state of _ 
		 * My phone number is_
		 */
		System.out.println("My name is "+name+ " and my last name is " +lastName);
		System.out.println("I am "+grade+ "student");
		System.out.println("I line in a city of "+city+ " in a state of "+state);
		System.out.println("My phone number is "+phoneNumber);
		

	}

}
