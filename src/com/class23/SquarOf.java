public class SquarOf {

	public int sqR(int a, int b) {
		System.out.println("The squar of rectangle is "+(a * b));
		return a+b;
	}
	public void sqS(int a) {
		System.out.println("The squar of square is "+(a*a));
		
	}
	public void sqC (int x, int y, int h) {
		System.out.println("The squar of cube is "+(x *y*h));
		
	}
}
