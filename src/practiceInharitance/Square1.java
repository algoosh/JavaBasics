package practiceInharitance;

public class Square1 {
	void sumOfSquar(int a) {
		System.out.println("The square of a square is " + (a * a));
	}

	void sumOfRectangle(int a, int b) {
		System.out.println("The square of a square is " + (a * b));
	}

	void sumOfCube(int a, int b, int c) {
		System.out.println("The square of a cube is " + (a * b * c));
	}
}
